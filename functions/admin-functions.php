/* Woo Text Trimmer */
/*-----------------------------------------------------------------------------------*/

function woo_text_trim($text, $words = 50)
{ 
	$matches = preg_split("/\s+/", $text, $words + 1);
	$sz = count($matches);
	if ($sz > $words) 
	{
		unset($matches[$sz-1]);
		return implode(' ',$matches)." ...";
	}
	return $text;
}


